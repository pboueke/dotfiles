local opt = vim.opt

-- Line numbers
opt.number = true
opt.relativenumber = true

-- Indentation
opt.tabstop = 2
opt.shiftwidth = 2
opt.expandtab = true
opt.smartindent = true

-- System clipboard
opt.clipboard = "unnamedplus"

-- Mouse
opt.mouse = "a"

-- No swap files
opt.swapfile = false
opt.backup = false

-- 24-bit colour
opt.termguicolors = true

-- Misc
opt.wrap = false
opt.scrolloff = 8
opt.signcolumn = "yes"
opt.updatetime = 250
